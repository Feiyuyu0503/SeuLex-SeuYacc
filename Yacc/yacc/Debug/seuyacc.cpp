				for (int i = 0;i < productions[num].numofrights;i++)
				{
					cout << productions[num].rightp[i];
					if (productions[num].rightp[0] != "eplsion")
					{
						mtokenstack.pop();
						moutbuffer.pop_back();
					}
					mnstack.pop();
					isused = false;
				}
				token a{ productions[num].leftp ,productions[num].leftp };
				tableAnalyse(mtokenstack, mnstack, moutbuffer, a, GOTO2);
